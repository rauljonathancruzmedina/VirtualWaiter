public void cargaitem() throws SQLException{
        funciones c = new funciones();
        user.removeAllItems(); 
        List<Usuario> l= c.usuarios();
        int x=0;
      while(x<(l.size())){
          user.addItem(l.get(x).getNombre());
          x++;
      }
}
